                <br/>3）预订成功后，如需退票，可前往个人中心：火车票->我的订单->订单详情进行退票，或者换取纸质车票后携带预订时所使用的乘车人有效身份证件原件，在列车开车前前往车站改签退票窗口办理相关手续）。
