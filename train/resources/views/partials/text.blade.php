                <h5><b> 第二条 取票说明</b></h5>
                1）若您使用二代居民身份证预订火车票产品，可凭预订时所使用的乘车人有效二代居民身份证原件到车站售票窗口、铁路客票代售点或车站自动售票机上办理换票手续。
                <br/>
                2）若您持有的二代居民身份证无法自动识读或者使用二代居民身份证以外的其他有效身份证件预订的，需出示预订时所使用的乘车人有效身份证件原件和窗口取票号， 到车站售票窗口或铁路运输企业授权的铁路客票代售点，由售票员录入证件号码和窗口取票号码并核实后办理换票手续。
                <br/>
                3）若您在预订成功后、换票前，不慎遗失有效身份证件，须由您本人到乘车站铁路公安制证口办理临时身份证明。
                <br/>
                4）有效身份证件信息、窗口取票号等经核实一致的，予以换票；不一致的，不予换票。
                注：部分动车组列车可持二代居民身份证直接检票进站。
